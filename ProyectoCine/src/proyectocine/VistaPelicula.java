    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            objPelicula = new Peliculas();
            Utilitarios util = new Utilitarios();
            ManagerPelicula managerPelicula = new ManagerPelicula();
            objPelicula.setNombre(txtNombrePe.getText());
            objPelicula.setIdTipoPelicula(lstTipoPelicula.get(cmbListaTPelicula.getSelectedIndex()));
            objPelicula.setFechaEstreno(util.cambiarStringDate(txtFechaIni.getText()));
            objPelicula.setFechaHasta(util.cambiarStringDate(txtFechaFin.getText()));
            objPelicula.setPeliculasActorList(lstPeliculasActors);
            lstPeliculasActors.forEach(actores->{
            actores.setIdPelicula(objPelicula);
            });
            managerPelicula.ingresarPelicula(objPelicula);
            JOptionPane.showMessageDialog(null, "Ingreso Correcto");
            txtFechaFin.setText("");
            txtFechaIni.setText("");
            txtNombrePe.setText("");
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
            Logger.getLogger(VistaPelicula.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        PeliculasActor objPeliculasActor = new PeliculasActor();
        objPeliculasActor.setIdActor(lstActores.get(cmbListaActores.getSelectedIndex()));
        lstPeliculasActors.add(objPeliculasActor);
        mostrarPeliculasActor();
    }//GEN-LAST:event_jButton2ActionPerformed

