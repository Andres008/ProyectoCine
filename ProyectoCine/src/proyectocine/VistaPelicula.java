    public void llenarComboActores()
    {
        try {
            ManagerActor managerActor = new ManagerActor();
            lstActores= managerActor.buscarTodosActor();
            lstActores.forEach(actores->{
                cmbListaActores.addItem(actores.getNombre()+" "+actores.getApellido());
            });
        } catch (Exception ex) {
            Logger.getLogger(VistaPelicula.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    public void llenarComboTipoPelicula(){
        try {
            ManagerPelicula managerPelicula = new ManagerPelicula();
            lstTipoPelicula=managerPelicula.buscarTodosTipoPelicula();
            lstTipoPelicula.forEach(tipoPelicula->{
                cmbListaTPelicula.addItem(tipoPelicula.getTipo());
            });
        } catch (Exception ex) {
            Logger.getLogger(VistaPelicula.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
