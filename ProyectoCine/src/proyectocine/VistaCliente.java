    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            ManagerCliente managerCliente = new ManagerCliente();
            Cliente objCliente = new Cliente();
            objCliente.setCedula(txtCedula.getText());
            objCliente.setNombres(txtNombre.getText());
            objCliente.setApellidos(txtApellido.getText());
            objCliente.setDireccion(txtApellido.getText());
            validarClaveIguales(txtClave1.getText(), txtClave2.getText());
            objCliente.setCorreoElectronico(txtDireccion.getText());
            managerCliente.ingresarCliente(objCliente);
            txtCedula.setText("");
            txtApellido.setText("");
            txtClave1.setText("");
            txtClave2.setText("");
            txtCorreoElectronico.setText("");
            txtDireccion.setText("");
            txtNombre.setText("");
            listarAutoresTbl();
            JOptionPane.showMessageDialog(null, "Ingreso correcto.");
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
            Logger.getLogger(VistaCliente.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        try {
            objCliente = lstCliente.get( tblCliente.getSelectedRow());
            ManagerCliente managerCliente = new ManagerCliente();
            managerCliente.eliminarCliente(objCliente.getCedula());
            JOptionPane.showMessageDialog(null, "Eliminacion Correcta");
            listarAutoresTbl();
        } catch (Exception ex) {
            Logger.getLogger(VistaCliente.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

