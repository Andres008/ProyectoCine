    private void listarAutoresTbl(){
        crearModeloTabla();
        try {
            ManagerCliente managerCliente = new ManagerCliente();
            lstCliente = managerCliente.buscarTodosClientes();
            lstCliente.forEach(cliente->{
            Object datos[] = new Object[5];
              datos[0]= cliente.getCedula();
              datos[1]= cliente.getApellidos();
              datos[2]= cliente.getNombres();
              datos[3]= cliente.getDireccion();
              datos[4]= cliente.getCorreoElectronico();
              modelo.addRow(datos);
            });
        } catch (Exception ex) {
            Logger.getLogger(VistaCliente.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    private void crearModeloTabla(){
        modelo = new DefaultTableModel();
        modelo.addColumn("Cédula");
        modelo.addColumn("Apellidos");
        modelo.addColumn("Nombre");
        modelo.addColumn("Dirección");
        modelo.addColumn("Correo Electronico");
        tblCliente.setModel(modelo);
    }
